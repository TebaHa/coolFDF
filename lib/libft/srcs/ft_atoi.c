#include <libft.h>

int		ft_atoi(const char *str)
{
	short				s;
	unsigned long int	r;

	s = 1;
	r = 0;
	while (*str == ' ' || *str == '\n' || *str == '\v'
	|| *str == '\t' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
		s = -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str && *str >= '0' && *str <= '9')
	{
		if ((r > 922337203685477580 || (r == 922337203685477580
		&& (*str - '0') > 7)) && s == 1)
			return (-1);
		if ((r > 922337203685477580 || (r == 922337203685477580
		&& (*str - '0') > 8)) && s == -1)
			return (0);
		r = r * 10 + (*str - '0');
		str++;
	}
	return ((int)(s * r));
}
