#include <libft.h>

void	ft_lstadd(t_list **alst, t_list *new)
{
	t_list	*current;

	if (new)
	{
		if (*alst == NULL)
			*alst = new;
		else
		{
			current = *alst;
			new->next = current;
			*alst = new;
		}
	}
}
